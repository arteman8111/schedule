// Функция для переключения страниц
function switchPage(page) {
    // Скрываем все контейнеры
    document.getElementById('scheduleContainer').style.display = 'none';
    document.getElementById('subjectsContainer').style.display = 'none';
    document.getElementById('settingsContainer').style.display = 'none';
    
    // Показываем/скрываем навигацию по неделям
    document.getElementById('weekNav').style.display = page === 'schedule' ? 'block' : 'none';
    
    // Обновляем заголовок
    const titles = {
        'schedule': 'Расписание занятий',
        'subjects': 'Все предметы',
        'settings': 'Настройки'
    };
    document.getElementById('pageTitle').textContent = titles[page];
    
    // Показываем нужный контейнер
    document.getElementById(`${page}Container`).style.display = 'block';
    
    // Обновляем активную кнопку
    document.querySelectorAll('.footer-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    document.getElementById(`${page}Btn`).classList.add('active');
    
    // Сохраняем текущее состояние
    currentState.page = page;
    
    // Если переключились на страницу, которая требует обновления данных
    if (page === 'subjects') {
        renderSubjectsPage();
    } else if (page === 'settings') {
        renderSettingsPage();
    }
}

// Функция для сбора общей статистики
function collectGlobalStats(subjects) {
    globalStats = {
        totalHours: 0,
        lectures: 0,
        seminars: 0,
        practices: 0,
        exams: 0,
        diffchecks: 0
    };
    
    subjects.forEach(subject => {
        globalStats.totalHours += subject.totalHours;
        Object.entries(subject.types).forEach(([type, count]) => {
            if (type.toLowerCase().includes('лекц')) globalStats.lectures += count;
            else if (type.toLowerCase().includes('практич')) globalStats.practices += count;
            else if (type.toLowerCase().includes('зачет') || type.toLowerCase().includes('экзамен')) globalStats.exams += count;
            else if (type.toLowerCase().includes('диф')) globalStats.diffchecks += count;
        });
    });
}

